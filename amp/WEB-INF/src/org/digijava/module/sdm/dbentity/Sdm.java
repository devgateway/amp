public class Sdm {

    private Long id;
    private String siteId;
    private String instanceId;
    private String name;
    private Set<SdmItem> items;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public Set<SdmItem> getItems() {
        return items;
    }

    public void setItems(Set<SdmItem> items) {
        this.items = items;
    }

    public String getInstanceId() {
        return instanceId;
    }

    public void setInstanceId(String instanceId) {
        this.instanceId = instanceId;
    }

    public String getSiteId() {
        return siteId;
    }

    public void setSiteId(String siteId) {
        this.siteId = siteId;
    }
    public SdmItem getItemByIndex(Long index) {
        Set<SdmItem> sdmItems = this.getItems();
        SdmItem  result = null;

        for (SdmItem item : sdmItems) {
            if (item.getParagraphOrder().equals(index)) {
                result = item;
                break;
            }
        }
        return result;
    }
}
