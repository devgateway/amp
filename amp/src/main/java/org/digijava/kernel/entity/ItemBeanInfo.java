public class ItemBeanInfo {
    private String info;
    private String value;
    public ItemBeanInfo(String Info, String Value) {
        this.info = Info;
        this.value = Value;
    }

    public String getInfo() {
        return info;
    }

    public String getValue() {
        return value;
    }

    public void setInfo(String info) {
        this.info = info;
    }

    public void setValue(String Value) {
        this.value = Value;
    }

}
