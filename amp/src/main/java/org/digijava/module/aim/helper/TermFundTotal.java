package org.digijava.module.aim.helper ;


public class TermFundTotal
{
    private String totCommAmount;
    private String totDisbAmount;
    private String totPlannedDisbAmount;
    private String totExpAmount;

    public String getTotCommAmount() {
        return totCommAmount;
    }

    public String getTotDisbAmount() {
        return totDisbAmount;
    }

    public String getTotPlannedDisbAmount() {
        return totPlannedDisbAmount;
    }

    public String getTotExpAmount() {
        return totExpAmount;
    }

    public void setTotCommAmount(String s) {
        totCommAmount = s;
    }
    public void setTotDisbAmount(String s) {
        totDisbAmount = s;
    }

    public void setTotPlannedDisbAmount(String s) {
        totPlannedDisbAmount = s;
    }

    public void setTotExpAmount(String s) {
        totExpAmount = s;
    }

    
}
