package org.digijava.module.aim.helper ;

public class FiscalCalendar
{
    int startMonth ;
    int startDay ;  
    
    /**
     * @return
     */
    public int getStartDay() {
        return startDay;
    }

    /**
     * @return
     */
    public int getStartMonth() {
        return startMonth;
    }

    /**
     * @param i
     */
    public void setStartDay(int i) {
        startDay = i;
    }

    /**
     * @param i
     */
    public void setStartMonth(int i) {
        startMonth = i;
    }

}
